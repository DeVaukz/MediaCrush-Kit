#import "MCKServer.h"

//! The full URL for the default MediaCrush site.
NSString * const MCKDefaultServerBaseURL = @"https://mediacru.sh";

//! The path from the base location of a MediaCrush instance to the API
//! endpoint.
NSString * const MCKAPIEndpointPathComponent = @"api";


//---------------------------------------------------------------------------//
@interface MCKServer ()
@property (nonatomic, copy, readwrite) NSURL *baseURL;
@end


@implementation MCKServer

//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//
#pragma mark - Retrieving The Default Server
//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//

//|++++++++++++++++++++++++++++++++++++|//
+ (instancetype)defaultServer
{
	static MCKServer *DefaultServer = nil;
    
	static dispatch_once_t onceToken;
	dispatch_once(&onceToken, ^{
        NSURL *baseURL = nil;
        
        /* This environment variable can be used to debug API requests by
		 * redirecting them to a different URL. */
		NSString *endpoint = NSProcessInfo.processInfo.environment[@"MCK_API_ENDPOINT"];
		if (endpoint != nil)
            baseURL = [NSURL URLWithString:endpoint];
        
		DefaultServer = [[self alloc] initWithBaseURL:baseURL];
	});
	return DefaultServer;
}

//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//
#pragma mark - Initializing Custom Servers
//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//

//|++++++++++++++++++++++++++++++++++++|//
- (instancetype)initWithBaseURL:(NSURL *)baseURL
{
	self = [super init];
	if (self == nil) return nil;
    
    if (baseURL == nil)
        baseURL = [NSURL URLWithString:MCKDefaultServerBaseURL];
    
	_baseURL = baseURL;
    
	return self;
}

//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//
#pragma mark - Accessing Server Properties
//◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦◦//

//|++++++++++++++++++++++++++++++++++++|//
- (NSURL *)APIEndpoint
{ return [self.baseURL URLByAppendingPathComponent:MCKAPIEndpointPathComponent isDirectory:YES]; }

//|++++++++++++++++++++++++++++++++++++|//
- (NSURL *)baseWebURL
{ return self.baseURL; }

@end
