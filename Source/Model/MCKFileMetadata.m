+ (NSDictionary*)JSONKeyPathsByPropertyKey
{
	return @{
        @"dimensions": @"dimensions",
        @"hasAudio": @"has_audio",
        @"hasVideo": @"has_video",
    };
}

//|++++++++++++++++++++++++++++++++++++|//
+ (NSValueTransformer *)dimensionsJSONTransformer
{
	return [MTLValueTransformer reversibleTransformerWithForwardBlock:^id(NSDictionary *dictionary) {
        NSSize size;
        size.width = [dictionary[@"width"] floatValue];
        size.height = [dictionary[@"height"] floatValue];
        return [NSValue valueWithSize:size];
    } reverseBlock:^id(NSValue *value) {
        NSSize size = [value sizeValue];
        return @{
            @"height": @((int)size.width),
            @"width": @((int)size.height)
        };
    }];
}

